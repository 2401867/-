''' 9. Вычислить значение функции y при заданном значении аргумента x по формуле y=0, 
если x<=(-1), или y=1+x,если (-1)<x<=0, или y=1, если x>0. '''

def calculate_y(x):
    if x <= -1:
        y = 0
    elif -1 < x <= 0:
        y = 1 + x
    else:  # x > 0
        y = 1
    return y

# Проверка функции с переменными True и False
test_values = [-2, -1, -0.5, 0, 1]

for x in test_values:
    y = calculate_y(x)
    print(f"Для x = {x}, y = {y}")


# Объяснение кода:
# Функция calculate_y принимает аргумент x и вычисляет значениеy в зависимости от условий.
# Мы используем условные операторы if, elif и else для определения значения y.
# В конце программы мы проверяем функцию на нескольких тестовых значениях x и выводим результаты.

''' 9. Определить, сколько элементов заданного одновременного массива размером 8 больше среднего 
значения элементов этого массива. '''
# Определение массива
array_size = 8
array = []

# Ввод данных в массив
print("Введите 8 элементов массива:")
for i in range(array_size):
    value = float(input(f"Элемент {i + 1}: "))
    array.append(value)

# Расчет среднего значения
average = sum(array) / array_size
print(f"\nИсходный массив: {array}")
print(f"Среднее значение элементов массива: {average}")

# Подсчет элементов больше среднего значения
count_greater_than_average = sum(1 for x in array if x > average)

# Вывод результата
print(f"Количество элементов, больше среднего значения: {count_greater_than_average}")




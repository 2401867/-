''' 5. В соревнованиях по бегу принимают участие 30 спортсменов. Вводя по очереди результаты участников, определить, сколько из них выполнили заданный норматив.'''


# Определяем переменные
normative = 10  # Заданный норматив
count = 0  # Счетчик спортсменов, выполнивших норматив

# Цикл для ввода результатов 30 спортсменов
for i in range(30):
    result = int(input(f"Введите результат спортсмена {i + 1}: "))  # Ввод результата
    # Проверяем, выполнил ли спортсмен норматив
    if result >= normative:
        count += 1  # Увеличиваем счетчик, если норматив выполнен

# Выводим количество спортсменов, выполнивших норматив
print(f"Количество спортсменов, выполнивших норматив: {count}")
